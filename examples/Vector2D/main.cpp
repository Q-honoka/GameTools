#include <iostream>

// Vector2D.h をインクルード
#include "Vector2D.h"
/*
*	【サンプルコードについて】
*	このファイルはVector2Dの機能確認を目的として、 
*	生成AIを利用して作成したコードをベースに、
*	内容を確認・修正して使用しています。
*/

int main()
{
	//==============================================================
	// コンストラクタ
	//==============================================================

	Vector2D vecA;					// (0, 0)
	Vector2D vecB(3.0f, 4.0f);		// (3, 4)

	std::cout << "vecA = (" << vecA.x << ", " << vecA.y << ")" << std::endl;
	std::cout << "vecB = (" << vecB.x << ", " << vecB.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// 単項マイナス
	//==============================================================

	Vector2D vecC = -vecB;

	std::cout << "-vecB = (" << vecC.x << ", " << vecC.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// 加算
	//==============================================================

	Vector2D vecD(1.0f, 2.0f);
	Vector2D vecE(5.0f, 6.0f);

	Vector2D add = vecD + vecE;

	std::cout << "vecD + vecE = (" << add.x << ", " << add.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// 減算
	//==============================================================

	Vector2D sub = vecE - vecD;

	std::cout << "vecE - vecD = (" << sub.x << ", " << sub.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// スカラー倍
	//==============================================================

	Vector2D mul1 = vecD * 2.0f;

	std::cout << "vecD * 2 = (" << mul1.x << ", " << mul1.y << ")" << std::endl;
	std::cout << std::endl;

	// 左側スカラー
	Vector2D mul2 = 3.0f * vecD;

	std::cout << "3 * vecD = (" << mul2.x << ", " << mul2.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// スカラー除算
	//==============================================================

	Vector2D div = vecE / 2.0f;

	std::cout << "vecE / 2 = (" << div.x << ", " << div.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// +=
	//==============================================================

	Vector2D addAssign(1.0f, 1.0f);

	addAssign += Vector2D(2.0f, 3.0f);

	std::cout << "+= : (" << addAssign.x << ", " << addAssign.y << ")" << std::endl;

	//==============================================================
	// -=
	//==============================================================

	Vector2D subAssign(5.0f, 5.0f);

	subAssign -= Vector2D(2.0f, 1.0f);

	std::cout << "-= : (" << subAssign.x << ", " << subAssign.y << ")" << std::endl;

	//==============================================================
	// *=
	//==============================================================

	Vector2D mulAssign(2.0f, 3.0f);

	mulAssign *= 5.0f;

	std::cout << "*= : (" << mulAssign.x << ", " << mulAssign.y << ")" << std::endl;

	//==============================================================
	// /=
	//==============================================================

	Vector2D divAssign(10.0f, 20.0f);

	divAssign /= 2.0f;

	std::cout << "/= : (" << divAssign.x << ", " << divAssign.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// ==
	//==============================================================

	Vector2D sameA(1.0f, 2.0f);
	Vector2D sameB(1.0f, 2.0f);

	if (sameA == sameB)
	{
		std::cout << "sameA と sameB は同じ" << std::endl;
	}

	//==============================================================
	// !=
	//==============================================================

	Vector2D diffA(1.0f, 2.0f);
	Vector2D diffB(3.0f, 4.0f);

	if (diffA != diffB)
	{
		std::cout << "diffA と diffB は違う" << std::endl;
	}

	std::cout << std::endl;

	//==============================================================
	// Length
	//==============================================================

	Vector2D lengthVec(3.0f, 4.0f);

	std::cout << "Length = " << lengthVec.Length() << std::endl;

	//==============================================================
	// LengthSquared
	//==============================================================

	std::cout << "LengthSquared = " << lengthVec.LengthSquared() << std::endl;
	std::cout << std::endl;

	//==============================================================
	// Normalize
	//==============================================================

	Vector2D normalizeVec(3.0f, 4.0f);

	normalizeVec.Normalize();

	std::cout << "Normalize = (" << normalizeVec.x << ", " << normalizeVec.y << ")" << std::endl;

	//==============================================================
	// Normalized
	//==============================================================

	Vector2D normalizedVec(6.0f, 8.0f);

	Vector2D newVec = normalizedVec.Normalized();

	std::cout << "Normalized = (" << newVec.x << ", " << newVec.y << ")" << std::endl;

	std::cout << "元の値 = (" << normalizedVec.x << ", " << normalizedVec.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// IsZero
	//==============================================================

	Vector2D zeroVec;

	if (zeroVec.IsZero())
	{
		std::cout << "zeroVec はゼロベクトル" << std::endl;
	}

	//==============================================================
	// SetZero
	//==============================================================

	Vector2D temp(10.0f, 20.0f);

	temp.SetZero();

	std::cout << "SetZero = (" << temp.x << ", " << temp.y << ")" << std::endl;

	//==============================================================
	// Set
	//==============================================================

	temp.Set(100.0f, 200.0f);

	std::cout << "Set = (" << temp.x << ", " << temp.y << ")" << std::endl;
	std::cout << std::endl;

	//==============================================================
	// Dot
	//==============================================================

	Vector2D dotA(1.0f, 2.0f);
	Vector2D dotB(3.0f, 4.0f);

	std::cout << "Dot = " << Dot(dotA, dotB) << std::endl;
	std::cout << std::endl;

	//==============================================================
	// Distance
	//==============================================================

	Vector2D posA(0.0f, 0.0f);
	Vector2D posB(3.0f, 4.0f);

	std::cout << "Distance = " << Distance(posA, posB) << std::endl;

	//==============================================================
	// DistanceSquared
	//==============================================================

	std::cout << "DistanceSquared = " << DistanceSquared(posA, posB) << std::endl;

	return 0;
}